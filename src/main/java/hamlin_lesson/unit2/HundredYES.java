package hamlin_lesson.unit2;

public class HundredYES {

    public static void main(String[] args) {

        for(int i = 1; i <= 3; i++){ // Outer for loop

            for(int j = 1; j <= 3; j++){ // Inner for loop
                System.out.println(i + " " + j);
            }

        }

    }
}
